def add(a: int, b: int) -> int:
    """adds two numbers

    Args:
        a (int): number a
        b (int): number b

    Returns:
        int: result
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """subtracts two numbers

    Args:
        a (int): number a
        b (int): number b

    Returns:
        int: result
    """
    return a - b


def multiply(a: int, b: int) -> int:
    """multiplies two numbers

    Args:
        a (int): number a
        b (int): number b

    Returns:
        int: result
    """
    return a * b


def divide(a: int, b: int) -> float:
    """divides two numbers

    Args:
        a (int): number a
        b (int): number b

    Returns:
        int: result
    """
    return a / b
